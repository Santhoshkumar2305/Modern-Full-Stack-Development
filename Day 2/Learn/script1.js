//1.Tag selector
const h=document.getElementsByTagName("p");
console.log(h.a);

//2.Id selector
const i=document.getElementById("a");
console.log(i);

//3.Class selector
const c=document.getElementsByClassName("c");
console.log(c[0]);

//4.Query selector
const d=document.querySelector("p");
console.log(d);

//5.Query selectorAll
const e = document.querySelectorAll("p");
console.log(e[1]);

